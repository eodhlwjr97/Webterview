              type="text"
              required
            />
          </p>
          <p class="text-center">
            <button
              class="btn btn-lg"
              style="background-color: #f05454; color: white; margin: 10px"
              @click="joinSession()"
            >
              Join
            </button>
          </p>
        </div>
      </div>
    </div>

    <div id="session" v-if="session">
      <div id="session-header">
        <h3 id="session-title">{{ mySessionId }}</h3>
        <input
          class="btn btn-large btn-danger"
          type="button"
          id="buttonLeaveSession"
          @click="leaveSession"
          value="Î©¥Ï†ë Ï¢ÖÎ£å"
        />
        <input
          class="btn btn-large btn-success"
          type="button"
          id="buttonLeaveSession"
          @click="leaveSession"
          value="Îã§Ïùå ÏßàÎ¨∏"
        />
      </div>
      <!-- <div id="video-container" class="col-md-12"> -->
      <div id="video-container">
        <!-- <user-video
